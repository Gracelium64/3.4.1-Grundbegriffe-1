void main () {

int a = 42;                                               // a = 42
int b = (5 * ++a + 3) * (a-- - 40);                       // b = 654; a = 42
int c = (a++ * 3 + 2) * (++a - b ~/ 10);                  // c = -2688; a = 44
double d = (b * 1.5 + c) / (a - b % c);                   // d = 2.79836066
String e = "${a * 2}${b ~/ 10}";                          // e = "8865"
String f = (c * 3 + b ~/ 5).toString();                   // f = "-7934"
String g = e + f + d.toString();                          // g = "8865-79342.79836066"

List<int> h = [1, 2, 3, 4, 5];                            // h = [1, 2, 3, 4, 5]
h.add(a);                                                 // h = [1, 2, 3, 4, 5, 44]
h.addAll([7, 8, 9]);                                      // h = [1, 2, 3, 4, 5, 44, 7, 8, 9]
int i = h[h.length - 2] * h[2] + h.first;                 // i = 25

String j = (a + 10).toString() + (b ~/ 100).toString();   // j = "546"
int k = h[4] * (c ~/ b) + h[2];                           // k = -17

List<int> l = [a, b, c];                                  // l = [44, 654, -2688]
l.add(b ~/ 10);                                           // l = [44, 654, -2688, 65]
l.add(c % 100);                                           // l = [44, 654, -2688, 65, -88]  ///-88?12?///

String m = l[2].toString() + l[1].toString();             // m = -2688654
int n = m.length + l.length;                              // n = 13

bool o = g.length > m.length;                             // o = true
bool p = h[3] > l[2];                                     // p = true
int q = (h.length + l.length) * (j.length - 1);           // q = 28

print("a = $a");
print("b = $b");
print("c = $c");
print("d = $d");
print("e = $e");
print("f = $f");
print("g = $g");
print("h = $h");
print("i = $i");
print("j = $j");
print("k = $k");
print("l = $l");
print("m = $m");
print("n = $n");
print("o = $o");
print("p = $p");
print("q = $q");



}

                                                                                                    
                                                                                                    
                                                                                                    
